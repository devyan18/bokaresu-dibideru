export function getKatakanasGroupFamily () {
  const katakanasGroupFamily = []
  katakanas.forEach(katakana => {
    let family = katakanasGroupFamily.find(family => family.family === katakana.family)
    if (!family) {
      family = {
        family: katakana.family,
        katakanas: []
      }
      katakanasGroupFamily.push(family)
    }
    family.katakanas.push(katakana)
  })
  return katakanasGroupFamily
}

export function getKatakanasGroupVocal () {
  const katakanasGroupVocal = []
  katakanas.forEach(katakana => {
    let vocal = katakanasGroupVocal.find(vocal => vocal.vocal === katakana.vocal)
    if (!vocal) {
      vocal = {
        vocal: katakana.vocal,
        katakanas: []
      }
      katakanasGroupVocal.push(vocal)
    }
    vocal.katakanas.push(katakana)
  })
  return katakanasGroupVocal
}

export function getKatakanasGroupByVocal (vocal) {
  return katakanas.filter(katakana => katakana.vocal === vocal)
}
